#!/usr/bin/env node

/**
 * workflow-add-tool.mjs — deterministic scaffolder dla `/add-tool` prompt.
 *
 * Implementuje structural parts `.github/prompts/add-tool.prompt.md`:
 * waliduje inputs, generuje Zod schema + defineTool snippet który LLM
 * (Copilot) wkleja do `src/server-<connector>.ts`. Drafts CHANGELOG entry
 * i README table row. Output ląduje w staging file pod `docs/runs/`.
 *
 * Script NIE edytuje server-<connector>.ts bezpośrednio — insertion point
 * zależy od contextu który LLM rozumie (alphabetical group, near related
 * tools, write vs read section). Wartość skryptu to usunięcie token-burn
 * na shape decisions: każdy tool startuje z identycznym envelope.
 *
 * Usage:
 *   npm run workflow:add-tool -- \
 *     --connector=jira \
 *     --tool=get_sprint \
 *     --description="Fetch sprint metadata and issue summary." \
 *     --type=read
 *
 * Output:
 *   docs/runs/<date>-add-tool-<connector>-<tool>.md
 *
 * @see .github/prompts/add-tool.prompt.md
 * @see .github/instructions/mcp-server.instructions.md
 */
import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import nodePath from 'node:path';
import process from 'node:process';

const ROOT = process.cwd();
const ARGS = parseArgs(process.argv.slice(2));
const TODAY = new Date().toISOString().slice(0, 10);

const c = {
  dim: (s) => `\x1b[2m${s}\x1b[0m`,
  ok: (s) => `\x1b[32m${s}\x1b[0m`,
  warn: (s) => `\x1b[33m${s}\x1b[0m`,
  err: (s) => `\x1b[31m${s}\x1b[0m`,
  bold: (s) => `\x1b[1m${s}\x1b[0m`,
};

const SUPPORTED = ['jira', 'confluence', 'figma', 'sonar', 'gitlab'];
const USAGE =
  'Usage: npm run workflow:add-tool -- --connector=<name> --tool=<snake_case> --description="..." --type=read|write';

for (const key of ['connector', 'tool', 'description', 'type']) {
  if (!ARGS[key]) {
    process.stdout.write(`${c.err('Missing required arg:')} --${key}\n${c.dim(USAGE)}\n`);
    process.exit(1);
  }
}
const { connector, tool, description, type } = ARGS;

if (!SUPPORTED.includes(connector)) {
  process.stdout.write(`${c.err(`unknown connector "${connector}"`)} — supported: ${SUPPORTED.join(', ')}\n`);
  process.exit(1);
}
if (!/^[a-z][a-z0-9_]*$/.test(tool)) {
  process.stdout.write(`${c.err(`tool name must be snake_case (got "${tool}")`)}\n`);
  process.exit(1);
}
if (!['read', 'write'].includes(type)) {
  process.stdout.write(`${c.err(`type must be 'read' or 'write' (got "${type}")`)}\n`);
  process.exit(1);
}

const serverPath = nodePath.resolve(ROOT, 'src', `server-${connector}.ts`);
if (!existsSync(serverPath)) {
  process.stdout.write(`${c.err(`${nodePath.relative(ROOT, serverPath)} not found`)}\n`);
  process.exit(1);
}
const serverSrc = readFileSync(serverPath, 'utf8');
if (new RegExp(`['"\`]${connector}\\.${tool}['"\`]`).test(serverSrc)) {
  process.stdout.write(`${c.err(`tool "${connector}.${tool}" already in src/server-${connector}.ts`)}\n`);
  process.exit(1);
}

process.stdout.write(`${c.bold('▶ Pre-flight')}\n  ${c.ok('✓')} connector=${connector} tool=${tool} type=${type}\n`);

const camel = tool.replaceAll(/_([a-z])/g, (_, ch) => ch.toUpperCase());
const pascal = camel[0].toUpperCase() + camel.slice(1);
const writeGuard = type === 'write' ? `\n  assertWriteAllowed('${connector}.${tool}');\n` : '';
const dryRunField = type === 'write' ? `\n  dryRun: z.boolean().default(false),` : '';
const dryRunBranch =
  type === 'write'
    ? `\n      if (input.dryRun) {\n        return { data: { dryRun: true, planned: input }, _meta };\n      }`
    : '';

const snippet = `// ── ${connector}.${tool} (${type}) ──
const ${pascal}Input = z.object({${dryRunField}
  // TODO: define inputs (use existing Zod helpers like IssueKey, ProjectKey)
});

defineTool({
  name: '${connector}.${tool}',
  description: ${JSON.stringify(description)},
  inputSchema: ${pascal}Input,
  async handle(input, ctx) {${writeGuard}
    const started = Date.now();
    // TODO: implement (call upstream via http-client, reshape, budget-aware extract)${dryRunBranch}

    const _meta = {
      tokensEstimate: 0, // TODO: estimatePayloadTokens(data)
      correlationId: ctx.correlationId,
      server: '${getServerName(connector)}',
      tool: '${connector}.${tool}',
      durationMs: Date.now() - started,
    };
    return { data: /* TODO */ null, _meta };
  },
});
`;

const changelogEntry = `- **${connector}.${tool}** (${type}) — ${description}`;
const readmeRow = `| \`${connector}.${tool}\` | ${type} | ${description} |`;

const stagingPath = `docs/runs/${TODAY}-add-tool-${connector}-${tool}.md`;
const stagingAbs = nodePath.resolve(ROOT, stagingPath);

const manifest = `---
id: run.add-tool.${connector}.${tool}
title: Add tool ${connector}.${tool}
type: run
date: ${TODAY}
status: scaffolded
---

# Add tool: \`${connector}.${tool}\`

Generated by \`npm run workflow:add-tool\`. The LLM agent (Copilot) finishes the integration.

## Snippet to paste into \`src/server-${connector}.ts\`

Insert near the other ${type} tools, keeping alphabetical grouping if used.

\`\`\`ts
${snippet}\`\`\`

## CHANGELOG entry (\`[Unreleased]\` → \`Added\`)

${changelogEntry}

## README row (Tools table)

${readmeRow}

## Checklist (Copilot)

- [ ] Patch \`src/server-${connector}.ts\`: paste snippet, fill TODOs, wire upstream call via shared \`http-client\`.
- [ ] If write: confirm \`assertWriteAllowed\` is reachable and dry-run returns the planned body.
- [ ] Add spec coverage: happy path, 4xx, 5xx${type === 'write' ? ', dry-run, write-guard denial' : ''}.
- [ ] Update README \`## Tools\` table with the row above (alphabetical).
- [ ] Append CHANGELOG entry under \`[Unreleased]\` → \`Added\`.
- [ ] Run validation gate: \`npm run verify\`.
${type === 'write' ? '- [ ] Request security-auditor review (write tool — write-guard + leakage check).' : ''}

## Anti-patterns

- Do not ship without the spec.
- Do not bundle two new tools in one PR.
- Do not extract a shared util for a single use; copy a few lines first.

## Hand-off

Delegate to \`connector-author\` agent (\`.github/agents/connector-author.agent.md\`).
`;

mkdirSync(nodePath.dirname(stagingAbs), { recursive: true });
if (existsSync(stagingAbs)) {
  process.stdout.write(`\n${c.warn('⚠')} ${stagingPath} exists (skip)\n`);
} else {
  writeFileSync(stagingAbs, manifest, 'utf8');
  process.stdout.write(`\n${c.ok('✓')} wrote ${stagingPath}\n`);
}

process.stdout.write(`\n${c.bold('Next steps (LLM agent — Copilot picks up here):')}\n`);
const steps = [
  `Open ${c.bold(stagingPath)} for the manifest`,
  `Paste snippet into ${c.bold(`src/server-${connector}.ts`)}, fill TODOs`,
  `Add spec coverage as listed in the checklist`,
  `Update CHANGELOG and README per the manifest`,
  `Run ${c.bold('npm run verify')} before commit`,
];
steps.forEach((s, i) => process.stdout.write(`  ${i + 1}. ${s}\n`));

process.exit(0);

// ── helpers ────────────────────────────────────────────────────────────────

function parseArgs(argv) {
  const out = {};
  for (const a of argv) {
    const kv = /^--([\w-]+)(?:=(.*))?$/.exec(a);
    if (!kv) continue;
    out[kv[1]] = kv[2] ?? true;
  }
  return out;
}

function getServerName(con) {
  return `mcp-${con}`;
}
